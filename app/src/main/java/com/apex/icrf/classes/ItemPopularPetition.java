public class ItemPopularPetition {

    public String e_petition_number;
    public String petition_number;
    public String member_id;
    public String name;
    public String petition_title;
    public String support_members;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getE_petition_number() {
        return e_petition_number;
    }

    public void setE_petition_number(String e_petition_number) {
        this.e_petition_number = e_petition_number;
    }

    public String getPetition_number() {
        return petition_number;
    }

    public void setPetition_number(String petition_number) {
        this.petition_number = petition_number;
    }

    public String getMember_id() {
        return member_id;
    }

    public void setMember_id(String member_id) {
        this.member_id = member_id;
    }

    public String getPetition_title() {
        return petition_title;
    }

    public void setPetition_title(String petition_title) {
        this.petition_title = petition_title;
    }

    public String getSupport_members() {
        return support_members;
    }

    public void setSupport_members(String support_members) {
        this.support_members = support_members;
    }




}
